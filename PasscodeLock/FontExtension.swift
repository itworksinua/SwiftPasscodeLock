import UIKit

extension UIFont {
    static func robotoMedium(_ size: CGFloat) -> UIFont? {
        return UIFont(name: "Roboto-Medium", size: size)
    }
    
    static func robotoRegular(_ size: CGFloat) -> UIFont? {
        return UIFont(name: "Roboto-Regular", size: size)
    }
}
